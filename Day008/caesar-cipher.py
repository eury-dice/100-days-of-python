def caesar(text, shift, direction):
    cipher_text = ""
    message = "Here's the encoded result: "
    if direction == "decode":
        shift *= -1
        message = "Here's the decoded result: "

    for char in text:
        if char in alphabet: 
            index = alphabet.index(char) 
            new_index = (index + shift) % 26
            cipher_text += alphabet[new_index]
        else:
            cipher_text += char

    print(message + cipher_text)

